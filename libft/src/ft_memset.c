#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*ptr;
	unsigned char	val;

	ptr = s;
	val = (unsigned char)c;
	while (n--)
		*ptr++ = val;
	return (s);
}
