#include "libft.h"

int	ft_is_prime(int nb)
{
	int	i;

	if (nb == 2 || nb == 3 || nb == 5)
		return (1);
	else if (nb < 2 || nb % 2 == 0 || nb % 3 == 0 || nb % 5 == 0 || (nb % 6 != 1
			&& nb % 6 != 5))
		return (0);
	i = 7;
	while (i * i <= nb)
	{
		if (nb % i == 0 || nb % (i + 2) == 0)
			return (0);
		i += 6;
	}
	return (1);
}
